def _entry_metadata(entry: dict, *, source_key: str | None = None) -> dict:
    """Build per-chunk metadata from a legal knowledge entry."""
    metadata_keys = (
        "title",
        "category",
        "jurisdiction",
        "effective_date",
        "source_name",
        "source_url",
        "article_refs",
        "risk_tags",
        "contract_types",
    )
    metadata = {
        key: entry[key]
        for key in metadata_keys
        if key in entry and entry[key] not in (None, "", [])
    }
    if source_key:
        metadata["source_key"] = source_key
    return metadata


def seed_legal_knowledge() -> int:
    """
    Seed the vector database with legal knowledge.

    Returns:
        Number of chunks seeded
    """
    # First, store a synthetic "contract" entry for legal knowledge
    with get_connection() as conn:
        with conn.cursor() as cur:
            cur.execute("""
                INSERT INTO contracts (title, contract_type, lessor, lessee)
                VALUES ('法律知识库', '法律知识', '法律知识库', '法律知识库')
                RETURNING id
            """)
            contract_id = cur.fetchone()[0]
            conn.commit()

    # Chunk and store each legal knowledge entry
    total_chunks = 0
    for entry in LEGAL_KNOWLEDGE:
        chunks = _chunk_legal_entry(entry["content"], chunk_size=600)
        metadata = [_entry_metadata(entry) for _ in chunks]
        chunk_ids = store_contract_chunks(contract_id, chunks, metadata)
        total_chunks += len(chunk_ids)
        print(f"  Seeded: {entry['title']} ({len(chunks)} chunks)")

    return total_chunks


def _chunk_legal_entry(text: str, chunk_size: int = 600) -> List[str]:
    """Split legal text into chunks."""
    import re
    text = re.sub(r'\s+', ' ', text).strip()
    chunks = []
    start = 0
    text_len = len(text)

    while start < text_len:
        end = min(start + chunk_size, text_len)
        if end < text_len:
            # Try to break at sentence end
            for break_char in ['。', '；', '！', '？', '\n']:
                last_break = text.rfind(break_char, start + chunk_size // 2, end)
                if last_break > start + chunk_size // 3:
                    end = last_break + 1
                    break

        chunk = text[start:end].strip()
        if chunk:
            chunks.append(chunk)
        start = end

    return chunks


